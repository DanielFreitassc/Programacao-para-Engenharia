parede_a = float(input("Insira a largura da parede: "))
parede_l = float(input("Insira a altura da parede: "))
area = (parede_a * parede_l)
litros = area / 2
print(f"Sua parede tem a dimensão  de {parede_a} x {parede_l} e a sua área é de {area}m².")
print(f"Para pintar a parede, você precisa  de {litros}l de tintas.")

