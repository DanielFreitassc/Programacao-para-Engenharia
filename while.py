i = 0                     # Variavel 
while i <= 10:            # Enquanto variavel for menor que 10 o laço continuara.
    print(i)              # Mostra a variavel para o usuario.
    i += 1                # variavel + 1.
print("Fim da Contagem")  # Mostra uma mensagem ao final do laço de repetição. 

   